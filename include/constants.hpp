template <typename F>
constexpr F ceil(F num)
{
    return (static_cast<F>(static_cast<std::size_t>(num)) == num)
        ? num
        : num + ((num > 0) ? 1 : 0);
}

constexpr std::size_t log2(std::size_t n, std::size_t p = 0) 
{
  return (n <= 1) ? p : log2(n / 2, p + 1);
}

